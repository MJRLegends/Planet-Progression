	public boolean fillChests(World world, Random rand, BlockPos position) {
		int x = position.getX();
		int y = position.getY() - 1;
		int z = position.getZ();
		if (rand.nextInt(Config.worldgenStructureLootAmount) == 1) {
			System.out.println("Spawned loot!");
			TileEntityChest chest;
			chest = (TileEntityChest) world.getTileEntity(new BlockPos(x + 9, y + 1, z + 10));

			if (chest != null) {
				for (int j = 0; j < chest.getSizeInventory(); j++) {
					chest.setInventorySlotContents(j, null);
				}
				chest.setInventorySlotContents(0, ItemUtilities.getRandomItemStackFromItemList(PlanetProgression_Items.researchPapers));
			}
		}
		return false;
	}
